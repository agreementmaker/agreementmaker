package matching;

public class Instance {
	String uri;
	String label;
	
	public Instance(String uri) {
		this.uri = uri;
	}

	public String getUri() {
		return uri;
	}

	public String getLabel() {
		return label;
	}

	public void setLabel(String label) {
		this.label = label;
	}
	
	
}
